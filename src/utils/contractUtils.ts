/**
 * Mint a new DID token as an NFT
 */
export const mintDidToken = async (
  provider: ethers.providers.Web3Provider,
  tokenMetadata?: string
): Promise<{ hash: string; tokenId?: string }> => {
  try {
    // Use the provider from the useWallet hook
    if (!provider) {
      throw new Error("Provider not available");
    }

    // Create contract instance
    const contract = new ethers.Contract(
      CONTRACT_ADDRESS, 
      CONTRACT_ABI, 
      provider.getSigner()
    );
    
    // Default to placeholder URI if none provided
    const actualTokenURI = tokenMetadata || TOKEN_URI;
    
    // Call the mint function
    const tx = await contract.mint(actualTokenURI);
    console.log("Transaction submitted:", tx);
    
    // Wait for transaction to be mined
    const receipt = await tx.wait();
    console.log("Transaction receipt:", receipt);
    
    // Extract token ID from the Mint event
    let tokenId: string | undefined;
    
    // Find the Mint event in the logs
    const mintEvent = receipt.events?.find((event: any) => event.event === 'Mint');
    
    if (mintEvent && mintEvent.args) {
      tokenId = mintEvent.args.tokenId.toString();
      console.log("Minted Token ID from event:", tokenId);
    } else {
      // Fallback: try to parse logs manually
      const mintEventInterface = new ethers.utils.Interface(CONTRACT_ABI);
      for (const log of receipt.logs) {
        try {
          const parsed = mintEventInterface.parseLog(log);
          if (parsed.name === 'Mint') {
            tokenId = parsed.args.tokenId.toString();
            console.log("Minted Token ID from parsed log:", tokenId);
            break;
          }
        } catch (e) {
          // Not a Mint event, continue
        }
      }
    }

    return { hash: tx.hash, tokenId };
  } catch (error) {
    console.error("Contract interaction error:", error);
    throw error;
  }
};

/**
 * Create metadata for the DID token
 */
export const createDidMetadata = (
  idInfo: any,
  idImageUrl: string,
  selfieImageUrl: string,
  walletAddress: string
) => {
  // In a real application, you would create proper metadata
  // following a standard format like ERC-721 metadata schema
  return JSON.stringify({
    name: `Digital Identity for ${idInfo.fullName}`,
    description: "Digital Identity Document NFT",
    image: selfieImageUrl,
    attributes: [
      {
        trait_type: "Full Name",
        value: idInfo.fullName
      },
      {
        trait_type: "Date of Birth",
        value: idInfo.dateOfBirth
      },
      {
        trait_type: "Gender",
        value: idInfo.gender
      },
      {
        trait_type: "ID Number",
        value: idInfo.idNumber
      },
      {
        trait_type: "Wallet",
        value: walletAddress
      }
    ],
    id_document: idImageUrl
  });
}; 